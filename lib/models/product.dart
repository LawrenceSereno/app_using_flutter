class Product {
  final String name;
  final double price;
  final String image;
  final String description;
  final List<int> sizes;
  final String style;
  final String color;
  final String country;

  Product({
    required this.name,
    required this.price,
    required this.image,
    required this.description,
    required this.sizes,
    required this.style,
    required this.color,
    required this.country,
  });
}